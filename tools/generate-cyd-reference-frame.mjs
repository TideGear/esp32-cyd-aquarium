import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import {
  PANEL_MODE_IDS,
  PANEL_MODES,
} from "../ui/src/lib/aquarium/constants.js";
import { createAquariumEngine } from "../ui/src/lib/aquarium/engine.js";
import { createFixedEnvironment } from "../ui/src/lib/aquarium/environment.js";
import {
  createAquariumFramebuffer,
  unpackColor,
} from "../ui/src/lib/aquarium/framebuffer.js";
import {
  drawCenteredPixelText,
  measurePixelText,
} from "../ui/src/lib/aquarium/pixel-text.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..");

const MODE_ID = PANEL_MODE_IDS.DEVICE_80X106;
const mode = PANEL_MODES[MODE_ID];
const environment = createFixedEnvironment();
const frameCount = 180;
const frameIntervalMs = 33;
const referenceClock = Object.freeze({
  time: "20:55",
  date: "FRI 05 JUN",
});

const drawReferenceClockOverlay = (framebuffer) => {
  const { width, height, foreground } = framebuffer;
  const centerX = Math.round(width / 2);
  const timeScale = width >= 74 ? 2 : 1;
  const topY = 3;
  const dateSize = measurePixelText(referenceClock.date, {
    scale: 1,
    letterSpacing: 1,
  });
  const bottomY = Math.max(topY + 5 * timeScale + 3, height - dateSize.height - 3);

  drawCenteredPixelText(foreground, referenceClock.time, centerX, topY, {
    scale: timeScale,
    letterSpacing: timeScale,
    color: { r: 224, g: 248, b: 238, a: 236 },
    shadowColor: { r: 0, g: 6, b: 8, a: 160 },
  });
  drawCenteredPixelText(foreground, referenceClock.date, centerX, bottomY, {
    scale: 1,
    letterSpacing: 1,
    color: { r: 132, g: 222, b: 190, a: 220 },
    shadowColor: { r: 0, g: 6, b: 8, a: 160 },
  });
};

const makeReferenceFrame = () => {
  const framebuffer = createAquariumFramebuffer(MODE_ID);
  const engine = createAquariumEngine({
    mode,
    environment,
    disableStateLoad: true,
  });

  engine.resize({ mode, framebuffer, environment });

  let composite = framebuffer.composite;
  let backgroundPixels = framebuffer.background.clonePixels();
  let foregroundPixels = framebuffer.foreground.clonePixels();
  for (let frame = 0; frame < frameCount; frame += 1) {
    const now = frame * frameIntervalMs;
    engine.update({
      now,
      delta: frame === 0 ? 0 : frameIntervalMs,
      framebuffer,
      environment,
      mode,
    });
    drawReferenceClockOverlay(framebuffer);
    backgroundPixels = framebuffer.background.clonePixels();
    foregroundPixels = framebuffer.foreground.clonePixels();
    composite = framebuffer.compositeLayers({ clearForeground: true });
  }

  return { framebuffer, engine, composite, backgroundPixels, foregroundPixels };
};

const formatHexBytes = (bytes, valuesPerLine = 18) => {
  const lines = [];
  for (let i = 0; i < bytes.length; i += valuesPerLine) {
    const slice = bytes.slice(i, i + valuesPerLine);
    lines.push(
      `  ${Array.from(slice)
        .map((value) => `0x${value.toString(16).padStart(2, "0")}`)
        .join(", ")},`,
    );
  }
  return lines.join("\n");
};

const makeHeader = ({ composite, foregroundPixels, engine }) => {
  const rgb888Bytes = new Uint8Array(mode.logicalWidth * mode.logicalHeight * 3);
  const layerBytes = new Uint8Array(mode.logicalWidth * mode.logicalHeight);

  for (let i = 0; i < composite.length; i += 1) {
    const color = unpackColor(composite[i] >>> 0);
    const foreground = unpackColor(foregroundPixels[i] >>> 0);
    rgb888Bytes[i * 3] = color.r;
    rgb888Bytes[i * 3 + 1] = color.g;
    rgb888Bytes[i * 3 + 2] = color.b;
    layerBytes[i] = foreground.a > 0 ? 1 : 0;
  }

  const nonBlackPixels = composite.reduce(
    (count, value) => {
      const color = unpackColor(value >>> 0);
      return count + (color.r === 0 && color.g === 0 && color.b === 0 ? 0 : 1);
    },
    0,
  );
  const foregroundPixelsCount = layerBytes.reduce(
    (count, value) => count + (value === 1 ? 1 : 0),
    0,
  );

  return `#pragma once

#include <Arduino.h>
#include <pgmspace.h>

// Generated by tools/generate-cyd-reference-frame.mjs.
// Source: JS DeviceAquariumPreview pipeline, mode=${MODE_ID}, frames=${frameCount}.
// Clock overlay is frozen intentionally: ${referenceClock.time} / ${referenceClock.date}.
constexpr uint16_t CYD_REFERENCE_FRAME_WIDTH = ${mode.logicalWidth};
constexpr uint16_t CYD_REFERENCE_FRAME_HEIGHT = ${mode.logicalHeight};
constexpr uint16_t CYD_REFERENCE_FRAME_PIXELS = ${
    mode.logicalWidth * mode.logicalHeight
  };
constexpr uint16_t CYD_REFERENCE_FRAME_RGB888_BYTES = CYD_REFERENCE_FRAME_PIXELS * 3;
constexpr uint16_t CYD_REFERENCE_FRAME_LAYER_BYTES = CYD_REFERENCE_FRAME_PIXELS;
constexpr uint16_t CYD_REFERENCE_FRAME_NONBLACK_PIXELS = ${nonBlackPixels};
constexpr uint16_t CYD_REFERENCE_FRAME_FOREGROUND_PIXELS = ${foregroundPixelsCount};
constexpr uint32_t CYD_REFERENCE_FRAME_SEED_SIGNATURE = 0x51f1f00d;
constexpr const char* CYD_REFERENCE_FRAME_MODE_ID = "${MODE_ID}";
constexpr uint8_t CYD_REFERENCE_LAYER_BACKGROUND = 0;
constexpr uint8_t CYD_REFERENCE_LAYER_FOREGROUND = 1;

const uint8_t CYD_REFERENCE_FRAME_RGB888[CYD_REFERENCE_FRAME_RGB888_BYTES] PROGMEM = {
${formatHexBytes(rgb888Bytes)}
};

const uint8_t CYD_REFERENCE_FRAME_LAYER[CYD_REFERENCE_FRAME_LAYER_BYTES] PROGMEM = {
${formatHexBytes(layerBytes, 36)}
};

`;
};

const makeSvgPreview = ({ composite }) => {
  const scale = 3;
  const width = mode.logicalWidth * scale;
  const height = mode.logicalHeight * scale;
  const radius = 1.29;
  const circles = [];

  for (let y = 0; y < mode.logicalHeight; y += 1) {
    for (let x = 0; x < mode.logicalWidth; x += 1) {
      const color = unpackColor(composite[y * mode.logicalWidth + x] >>> 0);
      if (color.a === 0 || (color.r === 0 && color.g === 0 && color.b === 0)) {
        continue;
      }
      circles.push(
        `<circle cx="${x * scale + scale / 2}" cy="${y * scale + scale / 2}" r="${radius}" fill="rgb(${color.r},${color.g},${color.b})" />`,
      );
    }
  }

  return `<svg xmlns="http://www.w3.org/2000/svg" width="${width}" height="${
    height + 2
  }" viewBox="0 0 ${width} ${height + 2}">
  <rect width="${width}" height="${height + 2}" fill="#000" />
  <g transform="translate(0 1)">
${circles.map((line) => `    ${line}`).join("\n")}
  </g>
</svg>
`;
};

const run = async () => {
  const frame = makeReferenceFrame();
  const header = makeHeader(frame);
  const svg = makeSvgPreview(frame);
  const headerPath = path.join(projectRoot, "include", "CydReferenceFrame.h");
  const svgPath = path.join(projectRoot, "docs", "cyd-reference-frame-preview.svg");
  const metaPath = path.join(projectRoot, "docs", "cyd-reference-frame-meta.json");

  await mkdir(path.dirname(headerPath), { recursive: true });
  await mkdir(path.dirname(svgPath), { recursive: true });
  await writeFile(headerPath, header, "utf8");
  await writeFile(svgPath, svg, "utf8");
  await writeFile(
    metaPath,
    `${JSON.stringify(
      {
        modeId: MODE_ID,
        logicalWidth: mode.logicalWidth,
        logicalHeight: mode.logicalHeight,
        physicalWidth: mode.physicalWidth,
        physicalHeight: mode.physicalHeight,
        frames: frameCount,
        frameIntervalMs,
        clock: referenceClock,
        environment,
        engine: frame.engine.getDebugSnapshot(),
      },
      null,
      2,
    )}\n`,
    "utf8",
  );

  console.log(`Generated ${path.relative(projectRoot, headerPath)}`);
  console.log(`Generated ${path.relative(projectRoot, svgPath)}`);
  console.log(`Generated ${path.relative(projectRoot, metaPath)}`);
};

run().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
